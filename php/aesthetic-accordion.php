          IS DYE-SUBLIMATION A DURABLE FINISH?
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse">
      <div class="panel-body">
        <p>Unlike decorative films or wraps, Decoral Dye-Sublimation becomes part of the powder-coated surface.  It will be as durable as the quality of the adhesion achieved with the powder-coated surface.</p>
