        <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne">
          HOW DOES DYE SUBLIMATION WORK?
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse">
      <div class="panel-body">
        <p>The dye sublimation process begins with our high quality pre-treatment. Prepared parts are them powder coated with specially formulated polyurethane powder to provide protection from wear, abrasion, impact and corrosion, and act as the relief base color for the final decoration.</p>
        <p>Once the part has been powder coated, it is wrapped with a sheet of non-porous material that has the desired decoration pattern printed on it using special high temperature inks. The specialized Decoral system is used to transfer the pattern from the paper to the base coat permeating the entire thickness of the coating. Since the design becomes a part of the powder coat, the decoration cannot be removed by normal wear, rubbing or scratching.</p>
      </div>
    </div>
