    <div id="collapseOne" class="panel-collapse collapse">
      <div class="panel-body">
        <p>The short answer is the thicker the better. The advantage of powder coating is its thickness. Products can be powder coated up to 15 to 20 mills and still attain strong adhesion using standard coatings. Specialized coatings can attain thickness of up to 125 mills. By contrast painting is limited to 2 to 4 mills. Other limitations are dictated by product design and function such as moving parts. In some cases the performance goals of your product can be achieved with a thinner surface but a product that offers a more durable finish. Your Advanced Finishing USA representative can provide recommendations to ensure your performance goals are met.</p>
