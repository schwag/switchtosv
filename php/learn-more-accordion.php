                                          <h1 class="needsbodyHeading">CLEANING:</h1>
                                          <p class="needsPar">To achieve maximum coating performance for your product, we clean parts using a cleaning method most suitable to the metallic make-up and surface condition. We clean aluminum parts to AAMA standards. Our wash lines use heated alkaline cleaners, heated iron phosphate pre-treatment, water rinses (using reverse osmosis filtered water), and bi-metal sealers. For products too large for our line we have a high-pressure steam cleaner that uses the same chemistry as our wash lines.</p>
