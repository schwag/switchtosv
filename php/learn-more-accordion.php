                                          <h1 class="needsbodyHeading">BURN-OFF:</h1>
                                          <p class="needsPar">To ensure the maximum cleanliness of the surface, we may place your parts in one of our DEP Approved burn-off ovens. This will remove any previous coating, machining oils or greases that may have accumulated during fabrication or machining.</p>
                                          <h1 class="needsbodyHeading">CHEMICAL STRIPPING:</h1>
                                          <p class="needsPar">For thin gauge, aluminum die cast, or parts that cannot withstand the heat of the burn-off oven, we can chemically strip the part to bare metal. This is especially important for parts requiring rework.</p>
                                          <h1 class="needsbodyHeading">GRINDING:</h1>
                                          <p class="needsPar">We may also prep parts by radiusing edges, grinding welds, and cleaning off weld spatters.</p>
                                          <h1 class="needsbodyHeading">SHOT BLAST OR GLASS BEADING:</h1>
                                          <p class="needsPar">For more aggressive pre-treatment we can sandblast using aluminum oxide to provide an SSPC-SP 5 white metal finish for maximum coating adhesion.</p>

