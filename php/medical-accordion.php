        <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne">
          HOW WILL ADDING ANTI-MICROBIAL COATINGS IMPACT COATING COSTS?
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse">
      <div class="panel-body">
        <p>The addition of anti-microbial additives to the powder will add approximately 10% to 20% to the cost of powder coating.</p>
      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo">
          HOW LONG WILL THE ANTI-MICROBIAL PROPERTIES LAST?
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse">
      <div class="panel-body">
        <p>Because the anti-microbial additive is homogeneously dispersed through the coating material it will last as long as the coating itself.  As the coating wears it exposes more anti-microbial material. As long as there is coating there is protection.</p>
      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseThree">
          WHAT ARE THE BENEFITS OF ANTI-MICROBIAL POWDER COATING OVER THE USE OF STAINLESS STEEL?
        </a>
      </h4>
    </div>
    <div id="collapseThree" class="panel-collapse collapse">
      <div class="panel-body">
        <p>Typically stainless steel products cost more than products made of another material then powder coated using anti-microbial additives.  In addition, Stainless Steel does not have anti-microbial properties, it is dependent on how effectively it is cleaned each time. Products that are powder coated with anti-microbial additives allow continuous reduction in microbe population. Anti microbial powder coating properties are always present on the coating.</p>
      </div>
